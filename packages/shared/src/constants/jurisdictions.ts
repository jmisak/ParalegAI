/**
 * Get all state codes
 */
export const STATE_CODES = Object.keys(JURISDICTIONS) as readonly string[];

/**
 * Get all community property states
 */
export const COMMUNITY_PROPERTY_STATES = Object.entries(JURISDICTIONS)
  .filter(([_, j]) => j.communityProperty)
  .map(([code]) => code) as readonly string[];

/**
 * Get all attorney-required states
 */
export const ATTORNEY_REQUIRED_STATES = Object.entries(JURISDICTIONS)
  .filter(([_, j]) => j.attorneyRequired)
  .map(([code]) => code) as readonly string[];

/**
 * Get states by closing type
 */
export const getStatesByClosingType = (type: ClosingType): readonly string[] =>
  Object.entries(JURISDICTIONS)
    .filter(([_, j]) => j.closingType === type)
    .map(([code]) => code);

/**
 * Get jurisdiction by code
 */
export const getJurisdiction = (code: string): Jurisdiction | undefined =>
  JURISDICTIONS[code.toUpperCase()];
