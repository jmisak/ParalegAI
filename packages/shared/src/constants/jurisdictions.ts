/**
 * US State jurisdictions with real estate law variants
 * @module constants/jurisdictions
 */

/**
 * Jurisdiction/state information
 */
export interface Jurisdiction {
  /** State code (2-letter) */
  code: string;
  /** Full state name */
  name: string;
  /** Closing type (attorney/title/escrow) */
  closingType: ClosingType;
  /** Whether attorney is required at closing */
  attorneyRequired: boolean;
  /** Whether state uses deed of trust or mortgage */
  securityInstrument: 'DEED_OF_TRUST' | 'MORTGAGE' | 'BOTH';
  /** Foreclosure type */
  foreclosureType: 'JUDICIAL' | 'NON_JUDICIAL' | 'BOTH';
  /** Community property state */
  communityProperty: boolean;
  /** Homestead exemption available */
  homesteadExemption: boolean;
  /** Transfer tax rate (per $1000) */
  transferTaxRate: number;
  /** Recording fee (approximate base) */
  baseRecordingFee: number;
  /** Attorney review period (days, 0 if none) */
  attorneyReviewPeriod: number;
  /** Typical due diligence period (days) */
  typicalDueDiligenceDays: number;
  /** Wet funding state (funds required at closing) */
  wetFunding: boolean;
  /** Additional notes */
  notes?: string;
}

/**
 * Closing type categories
 */
export type ClosingType = 'ATTORNEY' | 'TITLE' | 'ESCROW';

